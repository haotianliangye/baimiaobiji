import { create } from 'zustand';
import { db } from '../db/db';
import { generateUUID } from '../lib/utils';
import { useSettingsStore } from './settings.store';

interface AppState {
  isProcessingDiary: boolean;
  isProcessingReviewMap: Record<string, boolean>;
  diaryErrorMap: Record<string, string>;
  isSilentlyGenerating: boolean;
  autoGeneratedNotification: { date: string; diaryId: string } | null;
  generateDiaryTimeline: (dateStr: string, logs: any[], idToOverwrite?: string) => Promise<void>;
  generateReview: (id: string, dateStr: string, logs: any[], diaryContent: string) => Promise<void>;
  clearDiaryError: (dateStr: string) => void;
  silentlyGenerateDiary: (dateStr: string, logs: any[]) => Promise<void>;
  clearAutoGeneratedNotification: () => void;
}

export const useAppStore = create<AppState>((set, get) => ({
  isProcessingDiary: false,
  isProcessingReviewMap: {},
  diaryErrorMap: {},
  isSilentlyGenerating: false,
  autoGeneratedNotification: null,

  clearDiaryError: (dateStr) => {
    set((state) => {
      const newMap = { ...state.diaryErrorMap };
      delete newMap[dateStr];
      return { diaryErrorMap: newMap };
    });
  },

  generateDiaryTimeline: async (dateStr, logs, idToOverwrite) => {
    if (!logs || logs.length === 0) return;
    
    set({ isProcessingDiary: true });
    get().clearDiaryError(dateStr);
    
    const settings = { ...useSettingsStore.getState() };
    
    // Determine active prompt index. If regenerating, load the original diary's prompt settings
    let activePromptIndex = settings.diaryPromptIndex ?? 0;
    const promptNames = ['默认', '自定义 1', '自定义 2', '自定义 3'];
    let activePromptName = promptNames[activePromptIndex];
    
    try {
      if (idToOverwrite) {
        const originalDiary = await db.daily_diaries.get(idToOverwrite);
        if (originalDiary && originalDiary.prompt_index !== undefined) {
          activePromptIndex = originalDiary.prompt_index;
          activePromptName = originalDiary.prompt_name || promptNames[activePromptIndex];
          // Temporarily set settings index to use the original prompt for this regeneration
          settings.diaryPromptIndex = activePromptIndex;
        }
      }

      const response = await fetch('/api/generate-timeline', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          date: dateStr,
          timezone: Intl.DateTimeFormat().resolvedOptions().timeZone,
          logs: logs.map(l => ({ id: l.id, content: l.content, created_at: l.created_at })),
          settings
        })
      });
      
      if (!response.ok) {
         const errText = await response.text();
         let errData = { error: errText };
         try { errData = JSON.parse(errText); } catch(e){}
         throw new Error(errData.error || 'AI 服务响应异常，请重试');
      }
      
      const data = await response.json();
      
      // Store the ai_summary inside the timeline_json array for the summary display
      const timelineContent = JSON.stringify([{ summary: data.ai_summary || "暂无内容概要" }]);

      if (idToOverwrite) {
        await db.daily_diaries.update(idToOverwrite, {
           timeline_json: timelineContent,
           raw_log_ids: logs.map(l => l.id),
           ai_editorial: data.ai_editorial,
           ai_review: data.ai_review,
           updated_at: Date.now()
        });
      } else {
        await db.daily_diaries.add({
           id: generateUUID(),
           diary_date: dateStr,
           raw_log_ids: logs.map(l => l.id),
           timeline_json: timelineContent,
           ai_editorial: data.ai_editorial,
           ai_review: data.ai_review,
           updated_at: Date.now(),
           prompt_index: activePromptIndex,
           prompt_name: activePromptName
        });
      }
    } catch (err: any) {
      console.error("Timeline Generation Error:", err);
      set((state) => ({
        diaryErrorMap: {
          ...state.diaryErrorMap,
          [dateStr]: err.message || "生成时间轴失败，请检查网络连接"
        }
      }));
    } finally {
      set({ isProcessingDiary: false });
    }
  },

  generateReview: async (id, dateStr, logs, diaryContent) => {
    if (!logs || logs.length === 0) return;
    
    set((state) => ({
      isProcessingReviewMap: { ...state.isProcessingReviewMap, [id]: true }
    }));
    get().clearDiaryError(dateStr);
    
    const settings = useSettingsStore.getState();
    
    try {
      const response = await fetch('/api/generate-review', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          date: dateStr,
          timezone: Intl.DateTimeFormat().resolvedOptions().timeZone,
          logs: logs.map(l => ({ id: l.id, content: l.content, created_at: l.created_at })),
          diaryContent,
          settings
        })
      });
      
      if (!response.ok) {
         const errText = await response.text();
         let errData = { error: errText };
         try { errData = JSON.parse(errText); } catch(e){}
         throw new Error(errData.error || 'AI 服务响应异常，请重试');
      }
      
      const data = await response.json();
      
      // Update by specific ID
      const existing = await db.daily_diaries.get(id);
      if (existing) {
        await db.daily_diaries.update(id, {
           ai_review: data.ai_review,
           updated_at: Date.now()
         });
      } else {
        await db.daily_diaries.add({
           id: id,
           diary_date: dateStr,
           raw_log_ids: logs.map(l => l.id),
           timeline_json: JSON.stringify([{ summary: "暂无内容概要" }]),
           ai_editorial: "",
           ai_review: data.ai_review,
           updated_at: Date.now()
        });
      }
    } catch (err: any) {
      console.error("Review Generation Error:", err);
      set((state) => ({
        diaryErrorMap: {
          ...state.diaryErrorMap,
          [dateStr]: err.message || "生成回顾失败，请检查网络连接"
        }
      }));
    } finally {
      set((state) => ({
        isProcessingReviewMap: { ...state.isProcessingReviewMap, [id]: false }
      }));
    }
  },

  clearAutoGeneratedNotification: () => {
    set({ autoGeneratedNotification: null });
  },

  silentlyGenerateDiary: async (dateStr, logs) => {
    if (!logs || logs.length === 0) return;
    if (get().isSilentlyGenerating) return;
    
    set({ isSilentlyGenerating: true });
    
    const settings = { ...useSettingsStore.getState() };
    
    const activePromptIndex = settings.diaryPromptIndex ?? 0;
    const promptNames = ['默认', '自定义 1', '自定义 2', '自定义 3'];
    const activePromptName = promptNames[activePromptIndex];
    
    try {
      const response = await fetch('/api/generate-timeline', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          date: dateStr,
          timezone: Intl.DateTimeFormat().resolvedOptions().timeZone,
          logs: logs.map(l => ({ id: l.id, content: l.content, created_at: l.created_at })),
          settings
        })
      });
      
      if (!response.ok) {
        throw new Error('AI 服务响应异常');
      }
      
      const data = await response.json();
      const timelineContent = JSON.stringify([{ summary: data.ai_summary || "暂无内容概要" }]);
      const diaryId = generateUUID();

      await db.daily_diaries.add({
        id: diaryId,
        diary_date: dateStr,
        raw_log_ids: logs.map(l => l.id),
        timeline_json: timelineContent,
        ai_editorial: data.ai_editorial,
        ai_review: data.ai_review,
        updated_at: Date.now(),
        prompt_index: activePromptIndex,
        prompt_name: activePromptName
      });

      set({
        autoGeneratedNotification: {
          date: dateStr,
          diaryId: diaryId
        }
      });
    } catch (err: any) {
      console.error("Silent Timeline Generation Error:", err);
    } finally {
      set({ isSilentlyGenerating: false });
    }
  }
}));
